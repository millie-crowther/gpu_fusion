canon_sdf_t::canon_sdf_t(min_params_t * ps){
    voxel_length = ps->voxel_length;
    eta = ps->sdf_eta;    
    size = ps->size;

    for (int x = 0; x * voxel_length < size.x; x++){
        sdf.push_back(std::vector<std::vector<weight_sdf_t>>());

        for (int y = 0; y * voxel_length < size.y; y++){
            sdf[x].push_back(std::vector<weight_sdf_t>()); 

            for (int z = 0; z * voxel_length < size.z; z++){
                sdf[x][y].push_back(weight_sdf_t());
            }
        }
    }

    phi_global = new function_t<float>([=](point_t p){
        return distance(p);
    });
}

canon_sdf_t::~canon_sdf_t(){
    delete phi_global;
}

float 
canon_sdf_t::distance(point_t p){
    int x = p.x / voxel_length;
    int y = p.y / voxel_length;
    int z = p.z / voxel_length;

    if (
	x < 0 || y < 0 || z < 0 || 
	x >= sdf.size() || y >= sdf[0].size() || z >= sdf[0][0].size()
    ){
	return 1;
    } else if (sdf[x][y][z].omega == 0){
        return 1;
    } else {
        return sdf[x][y][z].phi / sdf[x][y][z].omega;
    }
}

void
canon_sdf_t::add_sdf(sdf_t * new_sdf){
    for (int x = 0; x < sdf.size(); x++){
        for (int y = 0; y < sdf[0].size(); y++){
            for (int z = 0; z < sdf[0][0].size(); z++){
                point_t p = (point_t(x, y, z) + point_t(0.5f)) * voxel_length;
                float phi = new_sdf->distance(p);
                sdf[x][y][z].phi += phi * weight(phi);
                sdf[x][y][z].omega += weight(phi);
            }
        }
    } 
}

float
canon_sdf_t::weight(float phi_true){
    return phi_true > -eta ? 1 : 0;
}

canon_sdf_t::triangle_t::triangle_t(point_t a, point_t b, point_t c){
    vertices[0] = a;
    vertices[1] = b;
    vertices[2] = c;
}

canon_sdf_t::cell_t::cell_t(point_t p, float l, canon_sdf_t * sdf){
    // convention used by Bourke is a bit strange but hey
    point[0] = p + point_t(0, 0, l);
    point[1] = p + point_t(l, 0, l);
    point[2] = p + point_t(l, 0, 0);
    point[3] = p;
    point[4] = p + point_t(0, l, l);
    point[5] = p + point_t(l);
    point[6] = p + point_t(l, l, 0);
    point[7] = p + point_t(0, l, 0);

    for (int i = 0; i < 8; i++){
        value[i] = sdf->distance(point[i]);
    }
}

void
canon_sdf_t::create_mesh(float isolevel, mesh_t * mesh){
    float l = voxel_length / 2;
    for (int x = 0; x < size.x; x += l){
        for (int y = 0; y < size.y; y += l){
            for (int z = 0; z < size.z; z += l){
                point_t p = point_t(x, y, z);
                cell_t cell(p, l, this);

                create_mesh_for_cell(isolevel, mesh, &cell);
            }
        }
    }
}

point_t
canon_sdf_t::normal(point_t p){
    return point_t(
        phi_global->differentiate(0)(p),
        phi_global->differentiate(1)(p),
        phi_global->differentiate(2)(p)
    );
}

void
canon_sdf_t::save_mesh(std::string filename){
    std::cout << "Saving SDF to mesh..." << std::endl;   

    // full filename
    std::string full_name = "../data/mesh/" + filename;

    // create triangles
    mesh_t mesh;
    create_mesh(0, &mesh);

    // create default material file
    std::ofstream mat_file;
    mat_file.open(full_name + ".mtl");
    mat_file << 
    "# Material file for " << filename  << std::endl <<
    "newmtl " << filename << "Material" << std::endl <<
    "Ns 96.078431"                      << std::endl <<
    "Ka 1.000000 1.000000 1.000000"     << std::endl <<
    "Kd 0.640000 0.640000 0.640000"     << std::endl <<
    "Ks 0.500000 0.500000 0.500000"     << std::endl <<
    "Ke 0.000000 0.000000 0.000000"     << std::endl <<
    "Ni 1.000000"                       << std::endl <<
    "d 1.000000"                        << std::endl <<
    "illum 2"                           << std::endl;
    mat_file.close();

    // save to wavefront .obj format
    std::ofstream mesh_file;
    mesh_file.open(full_name + ".obj");
    mesh_file <<
    "# Geometry file for " << filename << std::endl <<
    "mtllib " << filename << ".mtl"    << std::endl <<   
    "o " << filename << "Object"       << std::endl;

    // vertices
    for (auto tri : mesh){
        for (int i = 0; i < 3; i++){
            mesh_file << "v ";
            for (int j = 0; j < 3; j++){
		// centre at origin 
		// and account for peculiarities of blender coordinates
		point_t p = (tri.vertices[i] - size / 2.0f) / -100.0f;
                mesh_file << p[j] << " ";
            }
            mesh_file << std::endl;
        }
    }

    // normals 
    for (auto tri : mesh){
        for (int i = 0; i < 3; i++){
            mesh_file << "vn ";

            for (int j = 0; j < 3; j++){
                mesh_file << normal(tri.vertices[i])[j] << " ";
            }
            mesh_file << std::endl;
        }
    }

    mesh_file << 
    "usemtl " << filename << "Material" << std::endl <<
    "s off"                             << std::endl;          

    // faces
    for (int i = 0; i < mesh.size() * 3; i += 3){
        mesh_file << "f ";
        for (int j = 0; j < 3; j++){
            mesh_file << i+j+1 << "//" << i+j+1 << " ";
        }
        mesh_file << std::endl;
    }

    mesh_file.close(); 
}

